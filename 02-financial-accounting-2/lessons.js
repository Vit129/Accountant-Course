function parseNumberAfterEquals(text) {
  const match = text.match(/=\s*([\d,]+(?:\.\d+)?)/);
  if (!match) return null;
  return parseFloat(match[1].replace(/,/g, ''));
}

function approxEqual(a, b, tolerance) {
  return Math.abs(a - b) <= tolerance;
}

